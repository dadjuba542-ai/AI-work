const agents = db.prepare("SELECT id, name FROM agents").all() as { id: string; name: string }[];
let updated = 0;

for (const agent of agents) {
  const prompts = promptMap[agent.name];
  if (prompts) {
    db.prepare("UPDATE agents SET example_prompts = ? WHERE id = ?").run(
      JSON.stringify(prompts),
      agent.id
    );
    updated++;
    console.log(`✓ ${agent.name} → ${prompts.length} 个推荐问题`);
  } else {
    console.warn(`⚠ ${agent.name} → 未找到推荐问题`);
  }
}

console.log(`\n完成: 更新了 ${updated} 个 Agent`);
